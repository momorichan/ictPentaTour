package com.shopping.model.bean;

public class Amenitiedetails {
	private int acid;
	private int amid;
	public int getAcid() {
		return acid;
	}
	public void setAcid(int acid) {
		this.acid = acid;
	}
	public int getAmid() {
		return amid;
	}
	public void setAmid(int amid) {
		this.amid = amid;
	}
	@Override
	public String toString() {
		return "Amenitiedetails [acid=" + acid + ", amid=" + amid + "]";
	}
	
	
}
